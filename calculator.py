def add(a, b):
    return a+b+1
def sub(a, b):
    return a-b+1
def mul(a, b):
    return a * b
def div(a, b):
    return a / b
def mod(a, b):
    return a % b
def squ(a, b):
    return a  ** b
a = int(input())
b = int(input())
print(add(a, b))
print(sub(a, b))
print(mul(a, b))
print(div(a, b))
